    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed

		if (matricule.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Oops,Nothing to delete","Error", JOptionPane.ERROR_MESSAGE);
           return;
             }
        {
            int P = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirmation",JOptionPane.YES_NO_OPTION);
            if (P==0)
            {
                c = new DBConnection();
                try {
                    conn = c.getConnection();
                } catch (SQLException ex) {
                    Logger.getLogger(list2.class.getName()).log(Level.SEVERE, null, ex);
                }

                String sql= "delete from records_store3 where no_matricule = '" + matricule.getText() + "'";
                try {
                    pst=conn.prepareStatement(sql);
                } catch (SQLException ex) {
                    Logger.getLogger(list2.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    pst.execute();
                } catch (SQLException ex) {
                    Logger.getLogger(list2.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this,"Successfully deleted","Record",JOptionPane.INFORMATION_MESSAGE);
                Reset();
            }
        
        }
         Get_Data(); 
    
    }//GEN-LAST:event_deleteActionPerformed

