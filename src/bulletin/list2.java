    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        Get_Data();
        String fsearch=search.getText();
        if(!fsearch.isEmpty())
        {
            String sql="select no_matricule as 'no_matricule',names as 'names',dob as 'dob',sex as 'sex',class as 'class',year as 'year' from records_store3 where year=? or names=? or class=? or no_matricule=? order by year desc,class desc,no_matricule asc";
            try{
                pst=conn.prepareStatement(sql);
                pst.setString(1,String.valueOf( search.getText()));
                pst.setString(2,String.valueOf( search.getText()));
                pst.setString(3,String.valueOf( search.getText()));
                pst.setString(4,String.valueOf( search.getText()));
                ResultSet rs = pst.executeQuery();
                ctable.setModel(DbUtils.resultSetToTableModel(rs));
                pst.close();

            }catch(Exception ex){
                JOptionPane.showMessageDialog(this,ex);

            }}
    }//GEN-LAST:event_searchKeyReleased

